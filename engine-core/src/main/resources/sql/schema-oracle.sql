-- Workflow Definition Table (Master) - 사용자가 정의한 DAG의 본체
CREATE TABLE U_WF_DEFINITION (
    ID VARCHAR2(50),
    DEFINITION_NM VARCHAR2(100) NOT NULL,
    DESCRIPTION VARCHAR2(500),
    VERSION_NO NUMBER DEFAULT 1 NOT NULL,
    ACTIVE_FL VARCHAR2(1) DEFAULT 'Y' NOT NULL,
    USE_FL VARCHAR2(1) DEFAULT 'Y' NOT NULL,
    VIEW_FL VARCHAR2(1) DEFAULT 'Y' NOT NULL,
    DEL_FL VARCHAR2(1) DEFAULT 'N' NOT NULL,
    REG_DT DATE DEFAULT CURRENT_TIMESTAMP,
    REG_ID VARCHAR2(32),
    EDIT_DT DATE,
    EDIT_ID VARCHAR2(32),
    CONSTRAINT U_WF_DEFINITION_PK PRIMARY KEY (ID),
    CONSTRAINT U_WF_DEFINITION_U01 UNIQUE (DEFINITION_NM, VERSION_NO)
);

CREATE INDEX U_WF_DEFINITION_IDX01 ON U_WF_DEFINITION (ACTIVE_FL, DEL_FL);

-- Workflow Node Table (Master) - DAG 정의 내 노드(=액티비티 호출 단위)
CREATE TABLE U_WF_NODE (
    ID VARCHAR2(50),
    DEFINITION_ID VARCHAR2(50) NOT NULL,
    NODE_NM VARCHAR2(100),
    ACTIVITY_NM VARCHAR2(100) NOT NULL,
    INPUT_PARAMS CLOB,
    POS_X_NO NUMBER,
    POS_Y_NO NUMBER,
    USE_FL VARCHAR2(1) DEFAULT 'Y' NOT NULL,
    VIEW_FL VARCHAR2(1) DEFAULT 'Y' NOT NULL,
    DEL_FL VARCHAR2(1) DEFAULT 'N' NOT NULL,
    REG_DT DATE DEFAULT CURRENT_TIMESTAMP,
    REG_ID VARCHAR2(32),
    EDIT_DT DATE,
    EDIT_ID VARCHAR2(32),
    CONSTRAINT U_WF_NODE_PK PRIMARY KEY (ID),
    CONSTRAINT U_WF_NODE_FK01 FOREIGN KEY (DEFINITION_ID) REFERENCES U_WF_DEFINITION(ID)
);

CREATE INDEX U_WF_NODE_IDX01 ON U_WF_NODE (DEFINITION_ID);
CREATE INDEX U_WF_NODE_IDX02 ON U_WF_NODE (ACTIVITY_NM);

-- Workflow Edge Table (Master) - DAG 의존성 (FROM_NODE 완료 후 TO_NODE 활성화)
CREATE TABLE U_WF_EDGE (
    ID VARCHAR2(50),
    DEFINITION_ID VARCHAR2(50) NOT NULL,
    FROM_NODE_ID VARCHAR2(50) NOT NULL,
    TO_NODE_ID VARCHAR2(50) NOT NULL,
    CONDITION_EXPR VARCHAR2(500),
    USE_FL VARCHAR2(1) DEFAULT 'Y' NOT NULL,
    VIEW_FL VARCHAR2(1) DEFAULT 'Y' NOT NULL,
    DEL_FL VARCHAR2(1) DEFAULT 'N' NOT NULL,
    REG_DT DATE DEFAULT CURRENT_TIMESTAMP,
    REG_ID VARCHAR2(32),
    EDIT_DT DATE,
    EDIT_ID VARCHAR2(32),
    CONSTRAINT U_WF_EDGE_PK PRIMARY KEY (ID),
    CONSTRAINT U_WF_EDGE_FK01 FOREIGN KEY (DEFINITION_ID) REFERENCES U_WF_DEFINITION(ID),
    CONSTRAINT U_WF_EDGE_FK02 FOREIGN KEY (FROM_NODE_ID) REFERENCES U_WF_NODE(ID),
    CONSTRAINT U_WF_EDGE_FK03 FOREIGN KEY (TO_NODE_ID) REFERENCES U_WF_NODE(ID),
    CONSTRAINT U_WF_EDGE_U01 UNIQUE (FROM_NODE_ID, TO_NODE_ID)
);

CREATE INDEX U_WF_EDGE_IDX01 ON U_WF_EDGE (DEFINITION_ID);
CREATE INDEX U_WF_EDGE_IDX02 ON U_WF_EDGE (FROM_NODE_ID);
CREATE INDEX U_WF_EDGE_IDX03 ON U_WF_EDGE (TO_NODE_ID);

