ALTER TABLE H_WF_ACTIVITY_EXECUTION
    ADD CONSTRAINT H_WF_ACTIVITY_EXECUTION_FK02 FOREIGN KEY (NODE_ID) REFERENCES U_WF_NODE(ID);
