-- Line Instance Table (Master) - MariaDB
CREATE TABLE U_LINE_INSTANCE (
    ID VARCHAR(50),
    WORKFLOW_NAME VARCHAR(100) NOT NULL,
    STATUS_ST VARCHAR(20) NOT NULL, -- RUNNING, COMPLETED, FAILED, TERMINATED
    INPUT_DATA LONGTEXT, -- JSON format
    OUTPUT_DATA LONGTEXT, -- JSON format
    STATE_DATA LONGTEXT, -- JSON format (Line Context State)
    START_DT DATETIME,
    END_DT DATETIME,
    USE_FL VARCHAR(1) DEFAULT 'Y' NOT NULL,
    VIEW_FL VARCHAR(1) DEFAULT 'Y' NOT NULL,
    DEL_FL VARCHAR(1) DEFAULT 'N' NOT NULL,
    REG_DT DATETIME DEFAULT CURRENT_TIMESTAMP,
    REG_ID VARCHAR(32),
    EDIT_DT DATETIME,
    EDIT_ID VARCHAR(32),
    CONSTRAINT U_LINE_INSTANCE_PK PRIMARY KEY (ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- Activity Execution Table (History) - MariaDB
-- STATUS_ST: WAITING_DEPENDENCIES, PENDING, RUNNING, COMPLETED, FAILED
-- NODE_ID: DAG 모드에서 U_LINE_STATION 참조. 레거시(선형) 모드에서는 NULL.
CREATE TABLE H_LINE_ACTIVITY_EXECUTION (
    ID VARCHAR(50),
    INSTANCE_ID VARCHAR(50) NOT NULL,
    NODE_ID VARCHAR(50),
    ACTIVITY_NAME VARCHAR(100) NOT NULL,
    STATUS_ST VARCHAR(30) NOT NULL,
    INPUT_DATA LONGTEXT,
    OUTPUT_DATA LONGTEXT,
    ERROR_MESSAGE LONGTEXT,
    STACK_TRACE LONGTEXT,
    RETRY_CNT INT DEFAULT 0,
    NEXT_RETRY_DT DATETIME,
    START_DT DATETIME,
    END_DT DATETIME,
    USE_FL VARCHAR(1) DEFAULT 'Y' NOT NULL,
    VIEW_FL VARCHAR(1) DEFAULT 'Y' NOT NULL,
    DEL_FL VARCHAR(1) DEFAULT 'N' NOT NULL,
    REG_DT DATETIME DEFAULT CURRENT_TIMESTAMP,
    REG_ID VARCHAR(32),
    EDIT_DT DATETIME,
    EDIT_ID VARCHAR(32),
    CONSTRAINT H_LINE_ACTIVITY_EXECUTION_PK PRIMARY KEY (ID),
    CONSTRAINT H_LINE_ACTIVITY_EXECUTION_FK01 FOREIGN KEY (INSTANCE_ID) REFERENCES U_LINE_INSTANCE(ID)
    -- FK02 (NODE_ID → U_LINE_STATION) 는 U_LINE_STATION 생성 후 파일 끝에서 ALTER TABLE로 추가
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- Index for Worker Polling (MariaDB)
CREATE INDEX H_LINE_ACTIVITY_EXECUTION_IDX01 ON H_LINE_ACTIVITY_EXECUTION (STATUS_ST, NEXT_RETRY_DT);
CREATE INDEX H_LINE_ACTIVITY_EXECUTION_IDX02 ON H_LINE_ACTIVITY_EXECUTION (INSTANCE_ID);
CREATE INDEX H_LINE_ACTIVITY_EXECUTION_IDX03 ON H_LINE_ACTIVITY_EXECUTION (INSTANCE_ID, NODE_ID);

-- Dead Letter Queue Table (History) - MariaDB
CREATE TABLE H_LINE_DLQ (
    ID VARCHAR(50),
    INSTANCE_ID VARCHAR(50) NOT NULL,
    EXECUTION_ID VARCHAR(50),
    WORKFLOW_NAME VARCHAR(100) NOT NULL,
    ACTIVITY_NAME VARCHAR(100) NOT NULL,
    DLQ_STATUS_ST VARCHAR(20) NOT NULL, -- NEW, REQUEUED, DISCARDED
    ERROR_MESSAGE LONGTEXT,
    STACK_TRACE LONGTEXT,
    RETRY_CNT INT DEFAULT 0,
    MAX_RETRY_CNT INT,
    FAILED_AT_DT DATETIME,
    USE_FL VARCHAR(1) DEFAULT 'Y' NOT NULL,
    VIEW_FL VARCHAR(1) DEFAULT 'Y' NOT NULL,
    DEL_FL VARCHAR(1) DEFAULT 'N' NOT NULL,
    REG_DT DATETIME DEFAULT CURRENT_TIMESTAMP,
    REG_ID VARCHAR(32),
    EDIT_DT DATETIME,
    EDIT_ID VARCHAR(32),
    CONSTRAINT H_LINE_DLQ_PK PRIMARY KEY (ID),
    CONSTRAINT H_LINE_DLQ_FK01 FOREIGN KEY (INSTANCE_ID) REFERENCES U_LINE_INSTANCE(ID),
    CONSTRAINT H_LINE_DLQ_FK02 FOREIGN KEY (EXECUTION_ID) REFERENCES H_LINE_ACTIVITY_EXECUTION(ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- Indexes for DLQ (MariaDB)
CREATE INDEX H_LINE_DLQ_IDX01 ON H_LINE_DLQ (DLQ_STATUS_ST, REG_DT);
CREATE INDEX H_LINE_DLQ_IDX02 ON H_LINE_DLQ (INSTANCE_ID);

