CREATE TABLE U_LINE_TRACK (
    ID VARCHAR(50),
    DEFINITION_ID VARCHAR(50) NOT NULL,
    FROM_NODE_ID VARCHAR(50) NOT NULL,
    TO_NODE_ID VARCHAR(50) NOT NULL,
    CONDITION_EXPR VARCHAR(500),
    USE_FL VARCHAR(1) DEFAULT 'Y' NOT NULL,
    VIEW_FL VARCHAR(1) DEFAULT 'Y' NOT NULL,
    DEL_FL VARCHAR(1) DEFAULT 'N' NOT NULL,
    REG_DT DATETIME DEFAULT CURRENT_TIMESTAMP,
    REG_ID VARCHAR(32),
    EDIT_DT DATETIME,
    EDIT_ID VARCHAR(32),
    CONSTRAINT U_LINE_TRACK_PK PRIMARY KEY (ID),
    CONSTRAINT U_LINE_TRACK_FK01 FOREIGN KEY (DEFINITION_ID) REFERENCES U_LINE_DEFINITION(ID),
    CONSTRAINT U_LINE_TRACK_FK02 FOREIGN KEY (FROM_NODE_ID) REFERENCES U_LINE_STATION(ID),
    CONSTRAINT U_LINE_TRACK_FK03 FOREIGN KEY (TO_NODE_ID) REFERENCES U_LINE_STATION(ID),
    CONSTRAINT U_LINE_TRACK_U01 UNIQUE (FROM_NODE_ID, TO_NODE_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

